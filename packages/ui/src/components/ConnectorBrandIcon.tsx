/** Lucide icons when no public brand mark is available */
const LUCIDE_FALLBACK: Record<string, LucideIcon> = {
  graph_mcp: Network,
  mcp: Boxes,
  ticket: Ticket,
  git: GitBranch,
  book: BookOpen,
  cable: Cable,
  default: Puzzle,
};

function BrandSvg({
  brand,
  size,
}: {
  brand: BrandMark;
  size: number;
}) {
  return (
    <svg
      role="img"
      viewBox="0 0 24 24"
      width={size}
      height={size}
      aria-hidden
      xmlns="http://www.w3.org/2000/svg"
      style={{ display: "block", flexShrink: 0 }}
    >
      <title>{brand.label}</title>
      <path d={brand.path} fill={`#${brand.color}`} />
    </svg>
  );
}

export function ConnectorBrandIcon({
  type,
  size = 22,
  className = "",
}: {
  type: string;
  size?: number;
  className?: string;
}) {
  const brand = BRAND[type];
  if (brand) {
    return (
      <span
        className={`connector-icon connector-icon--brand ${className}`.trim()}
        title={brand.label}
        aria-hidden
      >
        <BrandSvg brand={brand} size={size} />
      </span>
    );
  }

  const Icon = LUCIDE_FALLBACK[type] ?? LUCIDE_FALLBACK.default!;
  return (
    <span
      className={`connector-icon connector-icon--lucide ${className}`.trim()}
      aria-hidden
    >
      <Icon size={size} strokeWidth={2.1} absoluteStrokeWidth />
    </span>
  );
}
